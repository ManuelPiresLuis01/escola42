#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	troc;

	i = 0;
	while (i < (size / 2))
	{
		troc = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = troc;
		i++;
	}
}
