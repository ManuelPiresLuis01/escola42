#include <stdio.h>
void	ft_swap(int *a, int *b)
{
	int	aux;

	aux = *a;
	*a = *b;
	*b = aux;
}

int main(void)
{	
	ft_swap(8, 7);
	printf("%d a: ",*a);
	printf("%d b: ",*b);
	return (0);
}


